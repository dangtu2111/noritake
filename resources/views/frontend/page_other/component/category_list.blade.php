										return cachedData ? JSON.parse(cachedData) : null;
									}

									// Hàm hiển thị HTML ra giao diện
									function renderHtml(dataArray) {
										var htmlArray = dataArray.map(function (item) {
											return `
                <div class='item-article d-flex align-items-center trai1'>
                    <div class='post-image'>
                        <a href='${item.url}' aria-label='${item.title}'>
                            <img src='${item.imageSrc}' alt='${item.title}'>
                        </a>
                    </div>
                    <div class='post-content'>
                        <p class='trai-title'>
                            <a href='${item.url}'>${item.title}</a>
                        </p>
                        <p class='post-meta'>
                            <a href='${item.url}'>${item.description}</a>
                        </p>
                    </div>
                </div>
            `;
										});

										// Chèn HTML vào phần tử đích
										$('.sidebox_content-list.list-blogs-latest.trai_list1').html(htmlArray.join(''));
									}

									// Tạo một khóa duy nhất cho bộ nhớ cache
									var cacheKey = 'randomUrlsCache';
									var cachedData = getFromCache(cacheKey);

									if (cachedData) {
										// Nếu dữ liệu đã được lưu trong sessionStorage, hiển thị ngay lập tức
										renderHtml(cachedData);
									} else {
										// Chọn ngẫu nhiên 7 URL từ danh sách
										var randomUrls = getRandomUrls(urls, 7);

										// Fetch dữ liệu từ tất cả các URL ngẫu nhiên
										Promise.all(randomUrls.map(fetchData)).then(function (results) {
											// Lọc ra các kết quả null
											var validResults = results.filter(item => item !== null);

											// Lưu kết quả hợp lệ vào bộ nhớ cache và hiển thị
											saveToCache(cacheKey, validResults);
											renderHtml(validResults);
										}).catch(function (error) {
											console.error('Failed to fetch one or more URLs', error);
										});
									}
								});

