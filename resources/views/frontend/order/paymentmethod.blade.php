	$(document).ready(function() {
		var total_due_price = $('.payment-due-price').attr('data-checkout-payment-due-target');
		if (total_due_price > 10000000) {
			$('#shipping_rate_id_1753806').prop('checked', true);
		} else {
			$('#shipping_rate_id_1753806').prop('checked', false);
		}

		$('.submit-order').click(function(e){
			e.preventDefault();
			var $button = $(this);
			if (!$button.hasClass('processing')) {
				$button.addClass('processing')
					.prop('disabled', true)
					.find('.btn-content').hide()
					.end()
					.find('.btn-spinner').show().css('opacity', 1);
				
				// Code xử lý khi click nút
				console.log('Nút đã được click');
				// Ví dụ: submit form
				// $(this).closest('form').submit();
			}
			var _token = $('meta[name="csrf-token"]').attr("content");
			var typeId = $('input[name="payment_method_id"]').attr('type-id');

			$.ajax({
				url: "{{ route('store.order') }}", // Thay bằng URL xử lý đơn hàng của bạn
				type: "POST",
				data: {
					_token: _token,
					full_name: {!! json_encode($order['full_name'] ?? '') !!},
					email: {!! json_encode($order['email'] ?? '') !!},
					phone: {!! json_encode($order['phone'] ?? '') !!},
					province_id: {!! json_encode($order['province_id'] ?? '') !!},
					district_id: {!! json_encode($order['district_id'] ?? '') !!},
					ward_id: {!! json_encode($order['ward_id'] ?? '') !!},
					province_name: {!! json_encode($order['province_name'] ?? '') !!},
					district_name: {!! json_encode($order['district_name'] ?? '') !!},
					ward_name: {!! json_encode($order['ward_name'] ?? '') !!},
					address: {!! json_encode($order['address'] ?? '') !!},
					discount_code: {!! json_encode($order['discount_code'] ?? '') !!},
					payment_method: typeId,
					note: {!! json_encode($order['note'] ?? '') !!},
					total_amount: {!! json_encode($order['total_amount'] ?? 0) !!}
				},
				beforeSend: function() {
					$(".btn-spinner").show();
				},
				success: function(response) {
					$('body .content .main').html(response)
				},
				error: function(xhr, status, error) {
					alert("Có lỗi xảy ra, vui lòng thử lại!");
					console.log(xhr.responseText);
				},
				complete: function() {
					$(".btn-spinner").hide();
				}
			});
		});
	});
</script>
